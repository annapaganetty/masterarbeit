function pknKe(p)
    function keFunc(e)
        a,b = ab(e)
        E = p.E
        h = p.h
        ν = p.ν
        
        D = (E*h^3) / (12*(1-ν^2))
        Ke = Array{Any}(undef,12,12)

        Ke[1,1] = Ke[4,4] = Ke[7,7] = Ke[10,10] = (20(a^4) + 14(a^2)*(b^2) + 20(b^4) - 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[1,2] = Ke[2,1] = Ke[10,11] = Ke[11,10] = (a^2 + 10(b^2) + 4(a^2)*ν) / (5(a^2)*b)
        Ke[1,3] = Ke[3,1] = Ke[4,6] = Ke[6,4] = (10(a^2) + b^2 + 4(b^2)*ν) / (5a*(b^2))
        Ke[1,4] = Ke[4,1] = Ke[7,10] = Ke[10,7] = (10(a^4) - 14(a^2)*(b^2) - 20(b^4) + 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[1,5] = Ke[5,1] = Ke[8,10] = Ke[10,8] = (a^2 + 10(b^2) - (a^2)*ν) / (5(a^2)*b)
        Ke[1,6] = Ke[3,4] = Ke[4,3] = Ke[6,1] = (5(a^2) - (b^2) - 4(b^2)*ν) / (5a*(b^2))
        Ke[1,7] = Ke[4,10] = Ke[7,1] = Ke[10,4] = (-10(a^4) + 14(a^2)*(b^2) - 10(b^4) - 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[1,8] = Ke[5,10] = Ke[8,1] = Ke[10,5] = (-(a^2) + 5(b^2) + (a^2)*ν) / (5(a^2)*b)
        Ke[1,9] = Ke[4,12] = Ke[9,1] = Ke[12,4] = (5(a^2) - (b^2) + (b^2)*ν) / (5a*(b^2))
        Ke[1,10] = Ke[4,7] = Ke[7,4] = Ke[10,1] = (-20(a^4) - 14(a^2)*(b^2) + 10(b^4) + 4(a^2)*(b^2)*ν) / (5(a^3)*(b^3))
        Ke[1,11] = Ke[2,10] = Ke[10,2] = Ke[11,1] = (-(a^2) + 5(b^2) - 4(a^2)*ν) / (5(a^2)*b)
        Ke[1,12] = Ke[4,9] = Ke[9,4] = Ke[12,1] = (10(a^2) + b^2 - (b^2)*ν) / (5a*(b^2))
        Ke[2,2] = Ke[5,5] = Ke[8,8] = Ke[11,11] = (4(a^2) + 20(b^2) - 4(a^2)*ν) / (15a*b)
        Ke[2,3] = Ke[3,2] = Ke[8,9] = Ke[9,8] = ν
        Ke[2,4] = Ke[4,2] = Ke[7,11] = Ke[11,7] = (-(a^2) - 10(b^2) + (a^2)*ν) / (5(a^2)*b)
        Ke[2,5] = Ke[5,2] = Ke[8,11] = Ke[11,8] = (-(a^2) + 10(b^2) + (a^2)*ν) / (15a*b)
        Ke[2,6] = Ke[2,9] = Ke[2,12] = Ke[3,5] = Ke[3,8] = Ke[3,11] = Ke[5,3] = Ke[5,9] = Ke[5,12] = Ke[6,2] = Ke[6,8] = Ke[6,11] = Ke[8,3] = Ke[8,6] = Ke[8,12] = Ke[9,2] = Ke[9,5] = Ke[9,11] = Ke[11,3] = Ke[11,6] = Ke[11,9] = Ke[12,2] = Ke[12,5] = Ke[12,8] = 0
        Ke[2,7] = Ke[4,11] = Ke[7,2] = Ke[11,4] = (a^2 - 5(b^2) - (a^2)*ν) / (5(a^2)*b)
        Ke[2,8] = Ke[5,11] = Ke[8,2] = Ke[11,5] = (a^2 + 5(b^2) - (a^2)*ν) / (15a*b)
        Ke[2,11] = Ke[5,8] = Ke[8,5] = Ke[11,2] = (-4(a^2) + 10(b^2) + 4(a^2)*ν) / (15a*b)
        Ke[3,3] = Ke[6,6] = Ke[9,9] = Ke[12,12] = (20(a^2) + 4(b^2) - 4(b^2)*ν) / (15a*b)
        Ke[3,6] = Ke[6,3] = Ke[9,12] = Ke[12,9] = (10(a^2) - 4(b^2) + 4(b^2)*ν) / (15a*b)
        Ke[3,7] = Ke[6,10] = Ke[7,3] = Ke[10,6] = (-5(a^2) + b^2 - (b^2)*ν) / (5a*(b^2))
        Ke[3,9] = Ke[6,12] = Ke[9,3] = Ke[12,6] = (5(a^2) + b^2 - (b^2)*ν) / (15a*b)
        Ke[3,10] = Ke[6,7] = Ke[7,6] = Ke[10,3] = (-10(a^2) - (b^2) + (b^2)*ν) / (5a*(b^2))
        Ke[3,12] = Ke[6,9] = Ke[9,6] = Ke[12,3] = (10(a^2) - (b^2) + (b^2)*ν) / (15a*b)
        Ke[4,5] = Ke[5,4] = Ke[7,8] = Ke[8,7] = (-(a^2) - 10(b^2) - 4(a^2)*ν) / (5(a^2)*b)
        Ke[4,8] = Ke[5,7] = Ke[7,5] = Ke[8,4] = (a^2 - 5(b^2) + 4(a^2)*ν) / (5(a^2)*b)
        Ke[5,6] = Ke[6,5] = Ke[11,12] = Ke[12,11] = -ν
        Ke[7,9] = Ke[9,7] = Ke[10,12] = Ke[12,10] = (-10(a^2) - (b^2) - 4(b^2)*ν) / (5a*(b^2))
        Ke[7,12] = Ke[9,10] = Ke[10,9] = Ke[12,7] = (-5(a^2) + b^2 + 4(b^2)*ν) / (5a*(b^2))
        return D * Ke
    end
    return keFunc
end