# Berechnung von Hx für ein Element e
function bMatrix(e)
    l₁₂,l₂₃,l₃₄,l₄₁,S₅,S₆,S₇,S₈,C₅,C₆,C₇,C₈ = geoH(e)

    V = [ -1 1 1 -1; -1 -1 1 1]
    Ns = serendipityelement(V)
    Ni = lagrangeelement(V)

    H = Array{Any}(undef,3, 12)
    H[1,1] = Ni[1]
    H[1,2] = 0
    H[1,3] = 0
    H[1,4] = Ni[2]
    H[1,5] = 0
    H[1,6] = 0
    H[1,7] = Ni[3]
    H[1,8] = 0
    H[1,9] = 0
    H[1,10] = Ni[4]
    H[1,11] = 0
    H[1,12] = 0

    H[2,1] = (-3Ns[5]*S₅*l₄₁ + 3Ns[8]*S₈*l₁₂) / (2l₁₂*l₄₁)
    H[2,2] = -0.75C₅*Ns[5]*S₅ - 0.75C₈*Ns[8]*S₈
    H[2,3] = Ns[1] + (1//16)*(8(C₅^2) - 4(S₅^2))*Ns[5] + (1//16)*(8(C₈^2) - 4(S₈^2))*Ns[8]
    H[2,4] = (3Ns[5]*S₅*l₂₃ - 3Ns[6]*S₆*l₁₂) / (2l₁₂*l₂₃)
    H[2,5] = -0.75C₅*Ns[5]*S₅ - 0.75C₆*Ns[6]*S₆
    H[2,6] = Ns[2] + (1//16)*(8(C₅^2) - 4(S₅^2))*Ns[5] + (1//16)*(8(C₆^2) - 4(S₆^2))*Ns[6]
    H[2,7] = (3Ns[6]*S₆*l₃₄ - 3Ns[7]*S₇*l₂₃) / (2l₂₃*l₃₄)
    H[2,8] = -0.75C₆*Ns[6]*S₆ - 0.75C₇*Ns[7]*S₇
    H[2,9] = Ns[3] + (1//16)*(8(C₆^2) - 4(S₆^2))*Ns[6] + (1//16)*(8(C₇^2) - 4(S₇^2))*Ns[7]
    H[2,10] = (3Ns[7]*S₇*l₄₁ - 3Ns[8]*S₈*l₃₄) / (2l₃₄*l₄₁)
    H[2,11] = -0.75C₇*Ns[7]*S₇ - 0.75C₈*Ns[8]*S₈
    H[2,12] = Ns[4] + (1//16)*(8(C₇^2) - 4(S₇^2))*Ns[7] + (1//16)*(8(C₈^2) - 4(S₈^2))*Ns[8]

    H[3,1] = (3C₅*Ns[5]*l₄₁ - 3C₈*Ns[8]*l₁₂) / (2l₁₂*l₄₁)
    H[3,2] = -Ns[1] + (1//16)*(4(C₅^2) - 8(S₅^2))*Ns[5] + (1//16)*(4(C₈^2) - 8(S₈^2))*Ns[8]
    H[3,3] = 0.75C₅*Ns[5]*S₅ + 0.75C₈*Ns[8]*S₈
    H[3,4] = (-3C₅*Ns[5]*l₂₃ + 3C₆*Ns[6]*l₁₂) / (2l₁₂*l₂₃)
    H[3,5] = -Ns[2] + (1//16)*(4(C₅^2) - 8(S₅^2))*Ns[5] + (1//16)*(4(C₆^2) - 8(S₆^2))*Ns[6]
    H[3,6] = 0.75C₅*Ns[5]*S₅ + 0.75C₆*Ns[6]*S₆
    H[3,7] = (-3C₆*Ns[6]*l₃₄ + 3C₇*Ns[7]*l₂₃) / (2l₂₃*l₃₄)
    H[3,8] = -Ns[3] + (1//16)*(4(C₆^2) - 8(S₆^2))*Ns[6] + (1//16)*(4(C₇^2) - 8(S₇^2))*Ns[7]
    H[3,9] = 0.75C₆*Ns[6]*S₆ + 0.75C₇*Ns[7]*S₇
    H[3,10] = (-3C₇*Ns[7]*l₄₁ + 3C₈*Ns[8]*l₃₄) / (2l₃₄*l₄₁)
    H[3,11] = -Ns[4] + (1//16)*(4(C₇^2) - 8(S₇^2))*Ns[7] + (1//16)*(4(C₈^2) - 8(S₈^2))*Ns[8]
    H[3,12] = 0.75C₇*Ns[7]*S₇ + 0.75C₈*Ns[8]*S₈

    return H
end




